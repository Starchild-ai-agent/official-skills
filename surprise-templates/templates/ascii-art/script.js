  /* ---------- Generate ASCII ---------- */
  function generateAscii(text, fontName) {
    var font = FONTS[fontName];
    if (!font) font = FONTS.standard;
    var upper = text.toUpperCase();
    var lines = [];
    for (var row = 0; row < font.height; row++) {
      var line = '';
      for (var i = 0; i < upper.length; i++) {
        var ch = upper[i];
        var charData = font.chars[ch];
        if (charData && charData[row]) {
          line += charData[row] + ' ';
        } else {
          line += '       ';
        }
      }
      lines.push(line);
    }
    return lines.join('\n');
  }

  function render() {
    var text = $('#textInput').value || 'Hello';
    var font = $('#fontSelect').value;
    var result = generateAscii(text, font);
    $('#output').textContent = result;
  }

  /* ---------- Copy ---------- */
  $('#copyBtn').addEventListener('click', function () {
    var text = $('#output').textContent;
    navigator.clipboard.writeText(text).then(function () {
      var btn = $('#copyBtn');
      btn.textContent = 'Copied!';
      setTimeout(function () { btn.textContent = 'Copy'; }, 1500);
    });
  });

